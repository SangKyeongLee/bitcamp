package step01;

public class Exam05_1 {
    public static void main(String[] args) {
        //10진수
        System.out.println(100);

        //8진수
        // 0 으로 시작
        System.out.println(0144);

        //2진수
        // 0b or 0B 로 시작
        System.out.println(0b1100100);

        //16진수
        //0x 로 시작
        System.out.println(0x64);
    }
}